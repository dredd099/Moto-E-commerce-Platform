    <h2>What Does the Warranty Cover?</h2>
    <ul>
        <li><strong>Manufacturing Defects:</strong> If your product has defects in materials or craftsmanship that affect its functionality or performance.</li>
        <li><strong>Material Wear:</strong> Problems like faulty zippers, torn seams, or structural integrity issues under normal use.</li>
        <li><strong>Operational Failures:</strong> Cases where a product doesn’t perform as advertised, such as a helmet visor mechanism failing prematurely.</li>
    </ul>
    <h2>What Isn’t Covered?</h2>
    <ul>
        <li>Normal wear and tear from regular use.</li>
        <li>Damage caused by misuse, neglect, or unauthorized modifications.</li>
        <li>Issues resulting from accidents or improper installation of parts.</li>
    </ul>
    <h2>How to File a Warranty Claim</h2>
    <ol>
        <li><strong>Gather Documentation:</strong> Keep your proof of purchase, such as an invoice or receipt, as it’s required for claims.</li>
        <li><strong>Contact MotoVault Support:</strong> Reach out to us via phone, email, or our live chat. Our team will guide you through the process.</li>
        <li><strong>Submit Details:</strong> Share photos and a detailed description of the issue. This helps us assess your claim quickly.</li>
        <li><strong>Resolution:</strong> Based on the claim, we’ll coordinate with the manufacturer to repair, replace, or refund your product.</li>
    </ol>
